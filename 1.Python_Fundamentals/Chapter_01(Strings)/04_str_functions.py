

name = "Prottoy"

print(name.upper())  # PROTTOY
print(name.lower())  # prottoy 
print(name.capitalize())  # Prottoy
print(name.title())  # Prottoy
print(name.swapcase())  # pROTTOY
print(len(name))  # 7
print(name.endswith("toy"))  # True
print(name.startswith("Pro"))  # True
print(name.count("o"))  # 2
print(name.find("o"))  # 1